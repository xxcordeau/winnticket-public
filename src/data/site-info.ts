function loadFromStorage(): SiteInfo {
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored) {
      const parsedData = JSON.parse(stored);
      // 
      if (!parsedData.terms || parsedData.terms.length === 0) {
        parsedData.terms = defaultSiteInfo.terms;
      }
      return parsedData;
    }
  } catch (error) {
  }
  return defaultSiteInfo;
}

// 
function saveToStorage(siteInfo: SiteInfo): void {
  try {
    localStorage.setItem(STORAGE_KEY, JSON.stringify(siteInfo));
  } catch (error) {
  }
}

// 
class SiteInfoStore {
  private siteInfo: SiteInfo;

  constructor() {
    this.siteInfo = loadFromStorage();
  }

  // 
  get(): ApiResponse<SiteInfo> {
    return {
      success: true,
      data: this.siteInfo,
      message: "사이트 정보 조회 성공",
      timestamp: new Date().toISOString(),
    };
  }

  // 
  update(data: Partial<Omit<SiteInfo, "id" | "updatedAt">>): ApiResponse<SiteInfo> {
    this.siteInfo = {
      ...this.siteInfo,
      ...data,
      updatedAt: new Date().toISOString(),
    };
    saveToStorage(this.siteInfo);
    return {
      success: true,
      data: this.siteInfo,
      message: "사이트 정보 수정 성공",
      timestamp: new Date().toISOString(),
    };
  }

  // 
  addBankAccount(account: Omit<BankAccount, "id">): ApiResponse<SiteInfo> {
    const newAccount: BankAccount = {
      ...account,
      id: `bank-${Date.now()}`,
    };
    this.siteInfo.bankAccounts.push(newAccount);
    this.siteInfo.updatedAt = new Date().toISOString();
    saveToStorage(this.siteInfo);
    return {
      success: true,
      data: this.siteInfo,
      message: "계좌 추가 성공",
      timestamp: new Date().toISOString(),
    };
  }

  // 
  updateBankAccount(id: string, data: Partial<Omit<BankAccount, "id">>): ApiResponse<SiteInfo> {
    const index = this.siteInfo.bankAccounts.findIndex((acc) => acc.id === id);
    if (index === -1) {
      return {
        success: false,
        data: null,
        message: "계좌를 찾을 수 없습니다.",
        timestamp: new Date().toISOString(),
      };
    }
    this.siteInfo.bankAccounts[index] = {
      ...this.siteInfo.bankAccounts[index],
      ...data,
    };
    this.siteInfo.updatedAt = new Date().toISOString();
    saveToStorage(this.siteInfo);
    return {
      success: true,
      data: this.siteInfo,
      message: "계좌 수정 성공",
      timestamp: new Date().toISOString(),
    };
  }

  // 
  deleteBankAccount(id: string): ApiResponse<SiteInfo> {
    const index = this.siteInfo.bankAccounts.findIndex((acc) => acc.id === id);
    if (index === -1) {
      return {
        success: false,
        data: null,
        message: "계좌를 찾을 수 없습니다.",
        timestamp: new Date().toISOString(),
      };
    }
    this.siteInfo.bankAccounts.splice(index, 1);
    this.siteInfo.updatedAt = new Date().toISOString();
    saveToStorage(this.siteInfo);
    return {
      success: true,
      data: this.siteInfo,
      message: "계좌 삭제 성공",
      timestamp: new Date().toISOString(),
    };
  }

  // 
  addTerms(terms: Omit<Terms, "id" | "createdAt" | "updatedAt">): ApiResponse<SiteInfo> {
    const newTerms: Terms = {
      ...terms,
      id: `terms-${Date.now()}`,
      createdAt: new Date().toISOString(),
      updatedAt: new Date().toISOString(),
    };
    this.siteInfo.terms.push(newTerms);
    this.siteInfo.updatedAt = new Date().toISOString();
    saveToStorage(this.siteInfo);
    return {
      success: true,
      data: this.siteInfo,
      message: "약관 추가 성공",
      timestamp: new Date().toISOString(),
    };
  }

  // 
  updateTerms(id: string, data: Partial<Omit<Terms, "id" | "createdAt">>): ApiResponse<SiteInfo> {
    const index = this.siteInfo.terms.findIndex((term) => term.id === id);
    if (index === -1) {
      return {
        success: false,
        data: null,
        message: "약관을 찾을 수 없습니다.",
        timestamp: new Date().toISOString(),
      };
    }
    this.siteInfo.terms[index] = {
      ...this.siteInfo.terms[index],
      ...data,
      updatedAt: new Date().toISOString(),
    };
    this.siteInfo.updatedAt = new Date().toISOString();
    saveToStorage(this.siteInfo);
    return {
      success: true,
      data: this.siteInfo,
      message: "약관 수정 성공",
      timestamp: new Date().toISOString(),
    };
  }

  // 
  deleteTerms(id: string): ApiResponse<SiteInfo> {
    const index = this.siteInfo.terms.findIndex((term) => term.id === id);
    if (index === -1) {
      return {
        success: false,
        data: null,
        message: "약관을 찾을 수 없습니다.",
        timestamp: new Date().toISOString(),
      };
    }
    this.siteInfo.terms.splice(index, 1);
    this.siteInfo.updatedAt = new Date().toISOString();
    saveToStorage(this.siteInfo);
    return {
      success: true,
      data: this.siteInfo,
      message: "약관 삭제 성공",
      timestamp: new Date().toISOString(),
    };
  }

  // 
  reset(): ApiResponse<SiteInfo> {
    this.siteInfo = { ...defaultSiteInfo };
    saveToStorage(this.siteInfo);
    return {
      success: true,
      data: this.siteInfo,
      message: "사이트 정보 초기화 성공",
      timestamp: new Date().toISOString(),
    };
  }
}

